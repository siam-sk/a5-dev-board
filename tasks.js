document.getElementById('task-btn-1').addEventListener('click', function(){
    alert('Board Updated Succesfully');

    this.disabled = true;

    assignedTasks = parseInt(document.getElementById('assigned-tasks').innerText);
    remainingTasks = assignedTasks - 1;
    document.getElementById('assigned-tasks').innerText = remainingTasks;

    if(assignedTasks <= 0){
        alert('All tasks have been completed.');
    }

    completedTasks = parseInt(document.getElementById('completed-tasks').innerText);
    totalTasks = completedTasks + 1;
    document.getElementById('completed-tasks').innerText = totalTasks;

    activityLog = document.getElementById('activity-log');
    const newParagraph = document.createElement('p');
    const currentTime = new Date().toLocaleTimeString();
    newParagraph.textContent = `You have completed Fix Mobile Button Issue task at ${currentTime}`;
    newParagraph.style.backgroundColor = 'aliceBlue';
    newParagraph.style.borderRadius = '5px';
    newParagraph.style.padding = '5px';
    newParagraph.style.margin = '5px';
    activityLog.appendChild(newParagraph);
});

